        private void btn_del_Click(object sender, EventArgs e)
        {
            if (MessageBox.Show("هل تريد حذف بيانات المورد المحدد ؟", "عملية الحذف", MessageBoxButtons.YesNo, MessageBoxIcon.Exclamation) == DialogResult.Yes)
            {
                db_model db = new db_model();
                Resource ac = (from c in db.Resources where c.ID == Resource_ID select c).FirstOrDefault();
                db.Resources.Where(a => a.ID == Resource_ID).Select(s => s).FirstOrDefault();
                db.Entry(ac).State = System.Data.Entity.EntityState.Deleted;

                db.SaveChanges();
                TR.Show_Message("تم حذف بيانات المورد");
                sqlDataSource1.Fill();
                SQL = ": تم حذف المورد   " + txtName.Text;
                TR.EnterLog(SQL);
            }
            else
            {
                TR.Show_Message("تم إلغاء عملية الحذف");
            }
            CLEAR_TEXT_BOX(this);
            
        }
        
