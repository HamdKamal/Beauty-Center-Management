        private void btn_save_Click(object sender, EventArgs e)
        {
            // this code for save data in database
            if (txtName.Text == string.Empty || txtPhone.Text == string.Empty)
            {
                TR.Show_Message("الرجاء إدخال جميع الحقول");
                return;
            }
            db_model db = new db_model();
            Resource ac = new Resource
            {
                Note = txtNote.Text,
                Name = txtName.Text,
                Phone = Convert.ToInt32( txtPhone.Text),
                Address = (txtAdd.Text)
            };
            db.Entry(ac).State = System.Data.Entity.EntityState.Added;
            db.SaveChanges();
            sqlDataSource1.Fill();
            TR.Show_Message("تم إضافة مورد جديد");
            groupBox1.Enabled = false;
            Amal(false);
            btn_save.Enabled = false;
            btn_new.Enabled = true;
            CLEAR_TEXT_BOX(this);
            SQL = ": تم إضافة مورد   " + txtName.Text;
            TR.EnterLog(SQL);
        }
